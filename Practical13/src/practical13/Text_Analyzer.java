    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String text = jTextPane1.getText();

    // Treat literal \t and \n as single spaces
    text = text.replace("\\t", "\t").replace("\\n", "\n");

    int length = text.length();
    int words = 0;
    int spaces = 0;
    int tabs = 0;
    int lines = 0;

    
    boolean inWord = false;

    for (int i = 0; i < text.length(); i++) {
        char c = text.charAt(i);

        if (c == ' ') spaces++;
        if (c == '\t') tabs++;
        if (c == '\n') lines++;

        if (Character.isLetterOrDigit(c)) {
            if (!inWord) {
                words++;
                inWord = true;
            }
        } else {
            inWord = false;
        }
    }

    if (text.length() > 0 && text.charAt(text.length() - 1) != '\n') {
        lines++;
    }

    jTextField2.setText("Length of text: " + length);
    jTextField3.setText("No. of Words: " + words);
    jTextField4.setText("No. of Spaces: " + spaces);
    jTextField5.setText("No. of Lines: " + lines);
    jTextField6.setText("No. of Tabs: " + tabs);

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
      String text = jTextPane1.getText();

    // Convert escape sequences to actual characters
    text = text.replace("\\t", "\t").replace("\\n", "\n");

    int[] letterCount = new int[26]; // For a-z
    int[] digitCount = new int[10];  // For 0-9
    char[] specialChars = new char[100]; 
    int[] specialCount = new int[100];
    int specialSize = 0;

    for (int i = 0; i < text.length(); i++) {
        char c = text.charAt(i);

        if (Character.isLetter(c)) {
            c = Character.toLowerCase(c);
            letterCount[c - 'a']++;
        } else if (Character.isDigit(c)) {
            digitCount[c - '0']++;
        } else if (!Character.isWhitespace(c)) {
            // Check if already exists in specialChars
            boolean found = false;
            for (int j = 0; j < specialSize; j++) {
                if (specialChars[j] == c) {
                    specialCount[j]++;
                    found = true;
                    break;
                }
            }
            if (!found && specialSize < 100) {
                specialChars[specialSize] = c;
                specialCount[specialSize] = 1;
                specialSize++;
            }
        }
    }

    // Build the result string
    StringBuilder result = new StringBuilder();

    result.append("ðŸ”¡ Letter Frequency:\n");
    for (int i = 0; i < 26; i++) {
        if (letterCount[i] > 0) {
            result.append((char) (i + 'a')).append(": ").append(letterCount[i]).append("\n");
        }
    }

    result.append("\nðŸ”¢ Digit Frequency:\n");
    for (int i = 0; i < 10; i++) {
        if (digitCount[i] > 0) {
            result.append(i).append(": ").append(digitCount[i]).append("\n");
        }
    }

    result.append("\nâœ¨ Special Character Frequency:\n");
    for (int i = 0; i < specialSize; i++) {
        result.append(specialChars[i]).append(": ").append(specialCount[i]).append("\n");
    }
    javax.swing.JOptionPane.showMessageDialog(this, result.toString());
    }//GEN-LAST:event_jComboBox1ActionPerformed

